    public int uniquePathsWithObstacles(int[][] obstacleGrid) {

        if(obstacleGrid == null ||
                obstacleGrid.length==0 ||
                obstacleGrid[0].length==0){
            return solution;
        }
        if(obstacleGrid[0][0] == 1){
            return 0;
        }
       //列数
        int m = obstacleGrid.length;
        int n = obstacleGrid[0].length;
        solution(obstacleGrid,0,0,m,n);
        return solution;
    }

    //m表示总列数，n表示总行数
    public void solution(int[][] obstacleGrid,int pm,int pn,int m,int n){
        methodAccessNum ++;
        //到达重点
        if(pm == m-1 && pn == n-1){
            solution ++;
            return ;
        }
        if(pm<m-1 && obstacleGrid[pm+1][pn] == 0){
            solution(obstacleGrid,pm+1,pn,m,n);
        }
        if(pn<n-1 && obstacleGrid[pm][pn+1] == 0){
            solution(obstacleGrid,pm,pn+1,m,n);
        }
    }
